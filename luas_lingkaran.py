# 1.(C) fungsi luas lingkaran 
def luaslingkaran(jarijari):
   return 3.14*(jarijari**2)
a = int(input("masukkan nilai jari-jari: "))

hasil = luaslingkaran(a)
print(hasil)

# 1.(D) fungsi mencari f(x)
# f(x) = 5x - 10 
def fungsi(x):
   return 5*x - 10
a = int(input("masukkan nilai x: "))
hasil = fungsi(a)
print(hasil)
